
// zadanie 1
var number = 1;
console.log(number); // 1
console.log(typeof number); // number

var string = "string";
console.log(string); // string
console.log(typeof string); // string

var numberString = 2 + "dwa";
console.log(numberString); // 2dwa
console.log(typeof numberString); // string

var boolean = true;
console.log(boolean); // true
console.log(typeof boolean); // boolean

var special = null;
console.log(special); // null
console.log(typeof special); // object - w pewnym sensie błąd specyfikacji
